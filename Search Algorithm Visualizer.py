    "            'A*': self.astar\n",
    "        }\n",
    "        if algo_name not in algorithms:\n",
    "            return None\n",
    "        start_time = time.time()\n",
    "        order, path, visited = algorithms[algo_name]()\n",
    "        elapsed = (time.time() - start_time) * 1000\n",
    "        return {\n",
    "            'order': order,\n",
    "            'path': path,\n",
    "            'visited': visited,\n",
    "            'nodes_explored': len(order) if order else 0,\n",
    "            'path_length': len(path) - 1 if path else 0,\n",
    "            'path_cost': len(path) - 1 if path else 0,\n",
    "            'time': elapsed,\n",
    "            'found': path is not None and len(path) > 0\n",
    "        }\n",
