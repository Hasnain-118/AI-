    "    def reset_grid(self):\n",
    "        \"\"\"Initialize empty grid\"\"\"\n",
    "        self.grid = np.zeros((self.rows, self.cols), dtype=int)\n",
    "        self.start = (2, 2)\n",
    "        self.end = (self.rows-3, self.cols-3)\n",
    "        self.grid[self.start] = 2\n",
    "        self.grid[self.end] = 3\n",
    "    \n",
    "    def toggle_wall(self, row, col):\n",
    "        if (row, col) == self.start or (row, col) == self.end:\n",
    "            return\n",
    "        self.grid[row, col] = 1 if self.grid[row, col] == 0 else 0\n",
    "    \n",
    "    def set_start(self, row, col):\n",
    "        if self.grid[row, col] == 1:\n",
    "            return\n",
    "        if self.start:\n",
    "            self.grid[self.start] = 0\n",
    "        self.start = (row, col)\n",
    "        self.grid[row, col] = 2\n",
    "    \n",
    "    def set_end(self, row, col):\n",
    "        if self.grid[row, col] == 1:\n",
    "            return\n",
    "        if self.end:\n",
    "            self.grid[self.end] = 0\n",
    "        self.end = (row, col)\n",
    "        self.grid[row, col] = 3\n",
    "    \n",
    "    def get_neighbors(self, pos):\n",
    "        r, c = pos\n",
    "        neighbors = []\n",
    "        for dr, dc in [(-1,0), (1,0), (0,-1), (0,1)]:\n",
    "            nr, nc = r + dr, c + dc\n",
    "            if 0 <= nr < self.rows and 0 <= nc < self.cols:\n",
    "                if self.grid[nr, nc] != 1:\n",
    "                    neighbors.append((nr, nc))\n",
    "        return neighbors\n",
    "    \n",
    "    def heuristic(self, pos):\n",
    "        return abs(pos[0] - self.end[0]) + abs(pos[1] - self.end[1])\n",
    "    \n",
    "    def bfs(self):\n",
    "        queue = deque([self.start])\n",
    "        visited = {self.start: None}\n",
    "        order = []\n",
    "        while queue:\n",
    "            current = queue.popleft()\n",
    "            order.append(current)\n",
    "            if current == self.end:\n",
    "                break\n",
    "            for neighbor in self.get_neighbors(current):\n",
    "                if neighbor not in visited:\n",
    "                    visited[neighbor] = current\n",
    "                    queue.append(neighbor)\n",
    "        path = self.reconstruct_path(visited)\n",
    "        return order, path, visited\n",
    "    \n",
    "    def dfs(self):\n",
    "        stack = [self.start]\n",
    "        visited = {self.start: None}\n",
    "        order = []\n",
    "        while stack:\n",
    "            current = stack.pop()\n",
    "            order.append(current)\n",
    "            if current == self.end:\n",
    "                break\n",
    "            for neighbor in self.get_neighbors(current):\n",
    "                if neighbor not in visited:\n",
    "                    visited[neighbor] = current\n",
    "                    stack.append(neighbor)\n",
    "        path = self.reconstruct_path(visited)\n",
    "        return order, path, visited\n",
    "    \n",
    "    def ucs(self):\n",
    "        pq = [(0, self.start)]\n",
    "        visited = {self.start: (0, None)}\n",
    "        order = []\n",
    "        while pq:\n",
    "            cost, current = heapq.heappop(pq)\n",
    "            order.append(current)\n",
    "            if current == self.end:\n",
    "                break\n",
    "            for neighbor in self.get_neighbors(current):\n",
    "                new_cost = cost + 1\n",
    "                if neighbor not in visited or new_cost < visited[neighbor][0]:\n",
    "                    visited[neighbor] = (new_cost, current)\n",
    "                    heapq.heappush(pq, (new_cost, neighbor))\n",
    "        path = []\n",
    "        if self.end in visited:\n",
    "            current = self.end\n",
    "            while current:\n",
    "                path.append(current)\n",
    "                current = visited[current][1]\n",
    "            path.reverse()\n",
    "        return order, path, visited\n",
    "    \n",
    "    def greedy(self):\n",
    "        pq = [(self.heuristic(self.start), self.start)]\n",
    "        visited = {self.start: None}\n",
    "        order = []\n",
    "        while pq:\n",
    "            _, current = heapq.heappop(pq)\n",
    "            order.append(current)\n",
    "            if current == self.end:\n",
    "                break\n",
    "            for neighbor in self.get_neighbors(current):\n",
    "                if neighbor not in visited:\n",
    "                    visited[neighbor] = current\n",
    "                    heapq.heappush(pq, (self.heuristic(neighbor), neighbor))\n",
    "        path = self.reconstruct_path(visited)\n",
    "        return order, path, visited\n",
    "    \n",
    "    def astar(self):\n",
    "        pq = [(self.heuristic(self.start), 0, self.start)]\n",
    "        visited = {self.start: (0, None)}\n",
    "        order = []\n",
    "        while pq:\n",
    "            f, g, current = heapq.heappop(pq)\n",
    "            order.append(current)\n",
    "            if current == self.end:\n",
    "                break\n",
    "            for neighbor in self.get_neighbors(current):\n",
    "                new_g = g + 1\n",
    "                if neighbor not in visited or new_g < visited[neighbor][0]:\n",
    "                    visited[neighbor] = (new_g, current)\n",
    "                    heapq.heappush(pq, (new_g + self.heuristic(neighbor), new_g, neighbor))\n",
    "        path = []\n",
    "        if self.end in visited:\n",
    "            current = self.end\n",
    "            while current:\n",
    "                path.append(current)\n",
    "                current = visited[current][1]\n",
    "            path.reverse()\n",
    "        return order, path, visited\n",
    "    \n",
    "    def reconstruct_path(self, visited):\n",
    "        path = []\n",
    "        if self.end in visited:\n",
    "            current = self.end\n",
    "            while current:\n",
    "                path.append(current)\n",
    "                current = visited[current]\n",
    "            path.reverse()\n",
    "        return path\n",
    "    \n",
    "    def run_algorithm(self, algo_name):\n",
    "        algorithms = {\n",
    "            'BFS': self.bfs,\n",
